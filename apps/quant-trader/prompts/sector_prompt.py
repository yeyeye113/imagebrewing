"""分板块 LLM Prompt — 不同板块使用不同分析规则。

板块分类:
  - 黑色系: 政策+库存+基建
  - 能化: EIA+OPEC+地缘
  - 贵金属: 美联储+美元+避险
  - 有色: 供需+库存+宏观
  - 农产品: 天气+种植+需求
  - 金融: 宏观+利率+流动性

用法:
    from quanttrader.prompts.sector_prompt import get_sector_prompt
    prompt = get_sector_prompt('RB')
"""

from __future__ import annotations

SECTOR_MAP = {
    "黑色系": ["I", "RB", "HC", "SS", "SF", "SM"],
    "能化": ["SC", "FU", "TA", "MA", "SA", "BU", "EG", "EB", "PP", "PG", "L", "V"],
    "贵金属": ["AU", "AG"],
    "有色": ["CU", "AL", "ZN", "NI", "SN", "SS", "PB"],
    "农产品": ["M", "P", "Y", "A", "RM", "C", "CS", "CF", "SR", "JD", "LH"],
    "金融": ["IF", "IC", "IH", "IM", "T", "TF", "TS", "TL"],
}


def get_sector(code: str) -> str:
    """获取品种所属板块。"""
    for sector, codes in SECTOR_MAP.items():
        if code.upper() in codes:
            return sector
    return "未知"


